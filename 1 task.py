# Ввод чисел через Memo (список)
numbers_input = input("Введите вещественные числа через пробел: ")

# Преобразование введенных данных в список вещественных чисел
numbers = list(map(float, numbers_input.split()))

# Сортировка списка по возрастанию
numbers.sort()

# Вывод отсортированного списка
print("Отсортированный массив:", numbers)
